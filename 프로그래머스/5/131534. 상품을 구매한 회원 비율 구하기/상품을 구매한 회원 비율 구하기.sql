SELECT YEAR(O.SALES_DATE) YEAR, MONTH(O.SALES_DATE) MONTH, COUNT(DISTINCT(O.USER_ID)) AS PUCHASED_USERS, ROUND(COUNT(DISTINCT(O.USER_ID)) / (select * from ENT), 1) AS PUCHASED_RATIO
FROM ONLINE_SALE O LEFT JOIN SUM ON O.USER_ID = SUM.USER_ID 
WHERE SUM.JOINED IS NOT NULL
GROUP BY YEAR, MONTH
ORDER BY YEAR, MONTH